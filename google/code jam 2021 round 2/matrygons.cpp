const int MN = 1e6 + 1;
int N,
    dp[MN];

vector<int> fct[MN];

int solve(int n) {
    if (n == 0) return 0;
    int &ret = dp[n];
    if (ret != -1) return ret;

    ret = 0;
    for (auto f : fct[n]) {
        if (n != N || f != 2)
            ret = max(ret, solve((n-f)/f)+1);
    }
    return ret;
}

void solve() {
    cin >> N;
    // for (auto i = 1; i <= N; i++) printf("i=%d dp=%d\n", i,solve(i));
    fill(dp, dp+MN, -1);
    cout << solve(N) << '\n';
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    for (auto i = 2; i < MN; i++) {
        for (auto j = i; j < MN; j += i)
            fct[j].push_back(i);
    }

    int T;
    cin >> T;
    for (int caseno = 1; caseno <= T; caseno++) {
        // Input
       
        cout << "Case #" << caseno << ": ";
        solve();

        // Reset
    }
}

